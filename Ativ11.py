for letra in input("coloque uma palavra: " ):
    if letra == "L":
        print ("L, e uma letra do meu nome")
    elif letra == "l":
        print ("l, e uma letra do meu nome")
    elif letra == "I":
        print ("I, e uma letra do meu nome")
    elif letra == "i":
        print ("i, e uma letra do meu nome")
    elif letra == "A":
        print ("A, e uma letra do meu nome")
    elif letra == "a":
        print ("a, e uma letra do meu nome")
    elif letra == "M":
        print ("M, e uma letra do meu nome")
    elif letra == "m":
        print ("m, e uma letra do meu nome")
    else:
        print(letra + " nao e uma letra do meu nome")
